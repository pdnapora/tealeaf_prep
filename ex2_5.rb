#write a paragraph that tells you the following:
# -hours in a year
# -minutes in a decade
# -your age in seconds

#output the number of hours in a year
puts 'there are ' + (24 * 365).to_s + ' hours in one year.'

#output the number of minutes in a decade
puts 'there are ' + (60 * 24 * 365 * 10).to_s + ' minutes in a decade.'

#output your age in seconds
puts 'i am approximately ' + (60 * 60 * 24 * 365 * 35).to_s + ' seconds old.'
